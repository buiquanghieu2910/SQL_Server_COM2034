CREATE DATABASE QLNT
GO

CREATE TABLE LOAINHA
(
MALN VARCHAR(10) NOT NULL,
TENLN NVARCHAR (50) NULL
PRIMARY KEY (MALN),
)
GO

CREATE TABLE QUANHUYEN
(
MAQH VARCHAR(10) NOT NULL,
TENQH NVARCHAR (50) NULL
PRIMARY KEY (MAQH),
)
GO

CREATE TABLE NGUOIDUNG
(
MAND VARCHAR(10) NOT NULL,
HOTENND NVARCHAR (50) NULL,
GIOITINH NVARCHAR (5) NULL,
SDT VARCHAR (15) NULL,
DIACHI NVARCHAR (50) NULL,
QUAN NVARCHAR (30) NULL,
EMAIL NVARCHAR (50) NULL
PRIMARY KEY (MAND),
)
GO

CREATE TABLE NHATRO
(
MANT VARCHAR(10) NOT NULL,
DIENTICH DECIMAL(8,2) NULL,
GIAPHONG MONEY NULL,
DIACHI NVARCHAR (50) NULL,
MOTA NVARCHAR(100) NULL,
NGAYDANGTIN DATETIME NULL,
MALN VARCHAR(10) NULL,
MAQH VARCHAR(10) NULL,
PRIMARY KEY (MANT),
CONSTRAINT FK_NHATRO_LOAINHA FOREIGN KEY (MALN) REFERENCES LOAINHA,
CONSTRAINT FK_NHATRO_QUANHUYEN FOREIGN KEY (MAQH) REFERENCES QUANHUYEN
)
GO

CREATE TABLE DANHGIA
(
MAND VARCHAR(10) NOT NULL,
MANT VARCHAR(10) NOT NULL,
[LIKE/DISLIKE] BIT NULL,
NOIDUNG NVARCHAR (50) NULL,

PRIMARY KEY (MANT,MAND),
CONSTRAINT FK_DANHGIA_NGUOIDUNG FOREIGN KEY (MAND) REFERENCES NGUOIDUNG,
CONSTRAINT FK_DANHGIA_NHATRO FOREIGN KEY (MANT) REFERENCES NHATRO
)
GO


